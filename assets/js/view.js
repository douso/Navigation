// 渲染菜单栏
function showMenu(menu) {
    let html = ''
    for (const item of menu) {
        let aStr = ` href="${item.link ? item.link : '#' + item.name}" class="smooth"`
        let subStr = ''

        // 二级菜单
        if (item.children) {
            aStr = ''
            subStr += '<ul>'
            for (const subItem of item.children) {
                const label = subItem.label ? `<span class="label pull-right hidden-collapsed ${subItem.labelClass}">${subItem.label}</span>` : ''
                subStr += `
                <li>
                    <a href="#${subItem.name}" class="smooth">
                        <span class="title">${subItem.name}</span>
                        ${label}
                    </a>
                </li>
                `
            }
            subStr += '</ul>'
        }

        const label = item.label ? `<span class="label pull-right hidden-collapsed ${item.labelClass}">${item.label}</span>` : ''
        // 一级菜单
        html += `
        <li>
            <a${aStr}>
                <i class="${item.icon}"></i>
                <span class="title">${item.name}</span>
                ${label}
            </a>
            ${subStr}
        </li>
        `
    }
    $("#main-menu").html(html)
}

// 渲染导航列表
function showNavList(navs) {
    let html = ''
    // 分类
    for (const item of navs) {
        html += `<h4 class="text-gray"><i class="linecons-tag" style="margin-right: 7px;" id="${item.name}"></i>${item.name}</h4>`
        html += `<div class="row">`
        // 卡片
        const list = item.list
        const len = list.length
        for (let i = 0; i < len; i++) {
            const pos = i + 1
            html += `
            <div class="col-sm-3">
                <div class="xe-widget xe-conversations box2 label-info" onclick="window.open('${list[i].link}', '_blank')">
                    <div class="xe-comment-entry">
                        <a class="xe-user-img">
                            <img data-src="assets/images/logos/${list[i].logo}" class="lozad img-circle" width="40">
                        </a>
                        <div class="xe-comment">
                            <a href="#" class="xe-user-name overflowClip_1">
                                <strong>${list[i].title}</strong>
                            </a>
                            <p class="overflowClip_2">${list[i].description}</p>
                        </div>
                    </div>
                </div>
            </div>
            `
            // 每四个换行显示
            if(pos!=len && pos%4==0){
                html += `</div><div class="row">`
            }
        }
        html += `</div><br/>`
    }
    $(".main-content nav").after(html)
}